public class Minion {
    String type;
    int maxHealth;
    int currentHealth;
    int level;

    public Minion() {}

    public String getType() {
        return type;
    }

    public int getMaxHealth() {
        return maxHealth;
    }

    public int getCurrentHealth() {
        return currentHealth;
    }

    public int getLevel() {
        return level;
    }
}
